    public function loadUserByIdentifier(string $usernameOrEmail): ?Doctor
    {
        $entityManager = $this->getEntityManager();

        return $entityManager->createQuery(
            'SELECT u
                FROM App\Entity\User u
                WHERE u.username = :query
                OR u.email = :query'
        )
            ->setParameter('query', $usernameOrEmail)
            ->getOneOrNullResult();
    }


    public function loadUserByUsername(string $username): ?Doctor
    {
        $entityManager = $this->getEntityManager();

        return $entityManager->createQuery(
            'SELECT u
                FROM App\Entity\User u
                WHERE u.username = :query
                '
        )
            ->setParameter('query', $username)
            ->getOneOrNullResult();
    }
